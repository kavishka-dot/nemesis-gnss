"""
nemesis.eval.report
===================
Generate a self-contained HTML training/evaluation report.
"""

from __future__ import annotations

from pathlib import Path
from typing import Dict, Any, Optional
import json
import numpy as np


def generate_report(
    results: Dict[str, Any],
    history: Optional[Dict[str, list]] = None,
    output_path: str | Path = "./nemesis_report.html",
) -> Path:
    """
    Generate a self-contained HTML evaluation report.

    Parameters
    ----------
    results : dict
        Output from ``evaluate_detector()``.
    history : dict, optional
        Training history dict with keys ``train_loss``, ``val_loss``, ``val_acc``.
    output_path : str or Path
        Where to save the HTML file.

    Returns
    -------
    Path to the saved report.
    """
    output_path = Path(output_path)
    cm = results.get("confusion_matrix", np.zeros((4, 4), dtype=int))
    class_names = ["Clear Sky", "Meaconing", "Slow Drift", "Adversarial"]

    cm_rows = ""
    for i, row in enumerate(cm):
        cells = "".join(
            f'<td style="background:rgba(0,200,255,{v / max(cm.max(), 1):.2f});padding:8px 14px;border-radius:4px">{v}</td>'
            for v in row
        )
        cm_rows += f"<tr><th>{class_names[i]}</th>{cells}</tr>"

    history_js = "null"
    if history:
        history_js = json.dumps(history)

    html = f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>NEMESIS Evaluation Report</title>
<script src="https://cdn.jsdelivr.net/npm/chart.js"></script>
<style>
  body {{ font-family: 'Courier New', monospace; background: #0a0e1a; color: #c8d6ef; margin: 0; padding: 2rem; }}
  h1 {{ color: #00d4ff; letter-spacing: 3px; border-bottom: 1px solid #1e3a5f; padding-bottom: .5rem; }}
  h2 {{ color: #7eb8f7; margin-top: 2rem; }}
  .metrics {{ display: flex; gap: 1.5rem; flex-wrap: wrap; margin: 1rem 0; }}
  .card {{ background: #111827; border: 1px solid #1e3a5f; border-radius: 8px; padding: 1.2rem 2rem; min-width: 150px; }}
  .card .value {{ font-size: 2rem; color: #00d4ff; font-weight: bold; }}
  .card .label {{ font-size: .8rem; color: #7eb8f7; text-transform: uppercase; letter-spacing: 1px; }}
  table {{ border-collapse: collapse; background: #111827; border-radius: 8px; overflow: hidden; }}
  th {{ background: #1e3a5f; color: #00d4ff; padding: 8px 14px; text-align: left; }}
  td {{ padding: 6px 14px; }}
  canvas {{ max-width: 600px; margin: 1rem 0; }}
  .footer {{ margin-top: 3rem; color: #3a5a7f; font-size: .75rem; border-top: 1px solid #1e3a5f; padding-top: 1rem; }}
</style>
</head>
<body>
<h1>NEMESIS · Evaluation Report</h1>

<div class="metrics">
  <div class="card"><div class="value">{results.get('accuracy', 0) * 100:.1f}%</div><div class="label">Accuracy</div></div>
  <div class="card"><div class="value">{results.get('f1_macro', 0):.3f}</div><div class="label">F1 Macro</div></div>
  <div class="card"><div class="value">{results.get('auc_ovr', 0):.3f}</div><div class="label">AUC OvR</div></div>
</div>

<h2>Confusion Matrix</h2>
<table>
  <tr><th>True \\ Pred</th>{''.join(f'<th>{n}</th>' for n in class_names)}</tr>
  {cm_rows}
</table>

<h2>Training History</h2>
<canvas id="lossChart"></canvas>
<canvas id="accChart"></canvas>

<div class="footer">
  Generated by nemesis-gnss &nbsp;|&nbsp; Apache 2.0 &nbsp;|&nbsp;
  github.com/kavishka-dot/nemesis-gnss
</div>

<script>
const history = {history_js};
if (history) {{
  const epochs = Array.from({{length: history.train_loss.length}}, (_, i) => i + 1);
  new Chart(document.getElementById('lossChart'), {{
    type: 'line',
    data: {{
      labels: epochs,
      datasets: [
        {{ label: 'Train Loss', data: history.train_loss, borderColor: '#00d4ff', tension: 0.3, pointRadius: 2 }},
        {{ label: 'Val Loss',   data: history.val_loss,   borderColor: '#ff6b6b', tension: 0.3, pointRadius: 2 }},
      ]
    }},
    options: {{ plugins: {{ legend: {{ labels: {{ color: '#c8d6ef' }} }} }}, scales: {{ x: {{ ticks: {{ color: '#7eb8f7' }} }}, y: {{ ticks: {{ color: '#7eb8f7' }} }} }}, backgroundColor: '#0a0e1a' }}
  }});
  new Chart(document.getElementById('accChart'), {{
    type: 'line',
    data: {{
      labels: epochs,
      datasets: [{{ label: 'Val Accuracy', data: history.val_acc.map(v => v * 100), borderColor: '#7bed9f', tension: 0.3, pointRadius: 2 }}]
    }},
    options: {{ plugins: {{ legend: {{ labels: {{ color: '#c8d6ef' }} }} }}, scales: {{ x: {{ ticks: {{ color: '#7eb8f7' }} }}, y: {{ ticks: {{ color: '#7eb8f7' }} }} }} }}
  }});
}}
</script>
</body></html>"""

    output_path.write_text(html, encoding="utf-8")
    return output_path
